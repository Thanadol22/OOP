package nokkhuntongbreeding;

public class NokkhunTong {
    private String name;
    private boolean isMale;

    public NokkhunTong(String name, boolean isMale) {
        this.name = name;
        this.isMale = isMale;
    }

    public String getName() {
        return name;
    }

    public boolean isMale() {
        return isMale;
    }
}


